package il.co.jb;

public class Calculator {
	
	public Calculator() {
		System.out.println("Created new calculator object");
	}

	public int add(int a, int b) {
		return a + b;
	}
	
	public int substruct (int a, int b) {
		return a - b;
	}
	
	public int multiply (int a, int b) {
		return a * b;
	}
	
	public double divide (int a, int b) {
		return a / b;
	}
	
	
}